//Lógica do cálculo	
function calcular(a, b, operacao) {
    let resultado;
    switch (operacao) {
        case "+":
            resultado = a + b;
            break;
        case "-":
            resultado = a - b;
            break;
        case "*":
            resultado = a * b;
            break;
        case "/":
            resultado = a / b;
            break;
        default:
            resultado = null;
    }
    return resultado;
    
}

